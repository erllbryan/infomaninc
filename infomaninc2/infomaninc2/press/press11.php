                    <h1 style="color:green;">Progress Software Strengthens RP Presence<br>
                                                Philippine Star, C4 - Networks <br>- <i>May 08, 2006</i>

                                              </h1>                        
                     <div class="intro">
                       <div class="tab1">
                             <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp Progress Software Corp., a supplier of application infrastructure software for the management, deployment and integration of business applications, has strengthened its local presence and is gearing up to expand its presence elsewhere in the Asia-Pacific region.

                            </div>
                             <br>

                              <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp At the launching of Progress Day 2006 Philippines at the New World Renaissance Hotel recently, Larry DiLoreto, VP for Asia-Pacific of Progress Software, unveiled the company's new global strategy in view of its recent acquisitions that are expected to considerably expand its product offerings.

                            </div>
                             <br>

                              <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp Buoyed by an inspiring performance in the Asia-Pacific in 2005, exceeding revenue targets and posting double-digit growth year on year for over 60 percent of its distributors, Progress is looking forward to raising its profile in the region by extending a more comprehensive support to companies utilizing Progress technologies, galvanizing its own developer and user communities, and marketing its products more aggressively

                              </div>
                             <br>
                             <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 
                            DiLoreto conceded though that Europe is still its number one market, although the Asia-Pacific is closing in. In 2005 alone, it gained 150 new customers in the region.  </div>
                          <br>


                              <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 
                            In a presentation, DiLoreto explained that Progress Software's operating units - Open Edge, Data Direct, Real Time and Sonic Software - have gotten a tremendous boost in recent years, thanks to the company's acquisitions that gave its customers the edge in state-of-the-art technology.  </div>
                            <br>

                            <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 
                            These acquisitions included EasyAsk, a leading natural language search and query; Persistence, an object-relationship mapping and caching mechanism; Apama, a leader in event stream processing with applications for online gaming and algorithmic trading; NEON Systems, a leader in mainframe data access and service-oriented architecture (SOA) integration; and Actional, which provides visibility and governance to SOAs.
                            </div><br>
                           <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 
                         Progress' goal, according to DiLoreto, is to maximize the benefits of information while minimizing total cost of ownership.
                             </div><br>
                           
                            <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 
                           Currently, over 60,000 organizations in over 135 countries, including nearly 80 percent of Fortune 100 companies, utilize over $5 billion of Progress technology and partner applications and services annually.
                             </div><br>
                 
                               <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 
                        Progress Software delivers products and services to end-user organizations via distributors and partnerships with independent software vendors
                             </div><br>
                         
