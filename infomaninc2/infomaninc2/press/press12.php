                                      <div>
                                   &nbsp&nbsp&nbsp&nbsp&nbsp&nbsp With the help of INFOMAN, Honda Cars Kalookan designed the IDMS that keeps customers satisfied with the dealer's service. "For the past six years, we have a customer satisfaction rating of 92 percent," he bared. Today Honda Cars Kalookan employs 100 technical staff from an original crop of 10 technicians. Its threestorey building can accommodate 350 cars from a humble beginning of 15 units.
