                              <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 
                            QAD solutions provide manufacturers with the solutions necessary to manage their core business processes. QAD's enterprise applications incorporate more than two decades of manufacturing expertise, enhanced by a team of global service professionals who help customers address their business needs and solve their challenges.


                            </div><br>


                        <div>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp 
                    MFG/PRO eB2 software is a comprehensive supply chain-enabled enterprise solution that includes manufacturing, sales and distribution, and financial management applications within an open systems environment. QAD has significantly increased the functionality of MFG/PRO eB2, expanding QAD's ability to be a supplier of powerful, industry-specific e-business solutions. Additionally, a new Web-based user interface, QAD Desktop 2, expands the ability of users to integrate MFG/PRO eB2 into their business practices. Rapidly installable, MFG/PRO eB2 provides single-site companies or multinational organizations with a fully integrated enterprise solution that sets new standards for connectivity, functionality, and ease-of-use. MFG/PRO eB2 delivers quick results because it is built from the ground up to meet the needs of the industries QAD serves. Because of QAD's Q-Advantage implementation methodology, MFG/PRO eB2 has one of the fastest implementation records in the business.
